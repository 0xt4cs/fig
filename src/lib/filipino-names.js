export function getRandomFilipino(array) {
  return array[Math.floor(Math.random() * array.length)];
}

export function generateFilipinoName(gender = null) {
  const isRandomGender = !gender;
  const selectedGender = gender || (Math.random() > 0.5 ? 'male' : 'female');
  
  const firstName = selectedGender === 'male' 
    ? getRandomFilipino(filipinoNames.maleFirstNames)
    : getRandomFilipino(filipinoNames.femaleFirstNames);
  
  const lastName = getRandomFilipino(filipinoNames.lastNames);
  
  return {
    firstName,
    lastName,
    fullName: `${firstName} ${lastName}`,
    gender: selectedGender
  };
}

export function generateFilipinoAddress() {
  const city = getRandomFilipino(filipinoNames.cities);
  const province = getRandomFilipino(filipinoNames.provinces);
  const barangay = getRandomFilipino(filipinoNames.barangays);
  
  // Generate a realistic Filipino street address
  const streetNumbers = ['123', '456', '789', '101', '202', '303', '555', '777', '888', '999'];
  const streetTypes = ['Street', 'Avenue', 'Road', 'Lane', 'Drive', 'Boulevard'];
  const commonStreetNames = ['Rizal', 'Bonifacio', 'Del Pilar', 'Luna', 'Mabini', 'Quezon', 'Roxas', 'Magsaysay', 'Marcos', 'Aguinaldo'];
  
  const streetNumber = getRandomFilipino(streetNumbers);
  const streetName = getRandomFilipino(commonStreetNames);
  const streetType = getRandomFilipino(streetTypes);
  
  return {
    street: `${streetNumber} ${streetName} ${streetType}`,
    barangay: `Brgy. ${barangay}`,
    city,
    province,
    country: 'Philippines',
    zipCode: String(Math.floor(Math.random() * 9000) + 1000)
  };
}

